const {crearServicio,editarServicio,buscarServicio,listarServicio,estadoServicio,eliminarServicio} = require('../models/servicioModels');

const listar=(req, res)=>{
    const id =  req.params.id;
    const sesId=req.params.sesId;
    listarServicio(id,'servicio',sesId)
    .then(valor => {
        res.json({
            valor : valor
        });
    })
    .catch(error => {
        res.status(400).json({
            error : {
                message:error.message,
                errno: error.errno,
                code : error.code
            }
        });
    }); 
}

