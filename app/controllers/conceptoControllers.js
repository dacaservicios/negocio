const {crearConcepto,editarConcepto,buscarConcepto,listarConcepto,estadoConcepto,eliminarConcepto} = require('../models/conceptoModels');

const listar=(req, res)=>{
    const id =  req.params.id;
    const sesId=req.params.sesId;
    listarConcepto(id,'concepto',sesId)
    .then(valor => {
        res.json({
            valor : valor
        });
    })
    .catch(error => {
        res.status(400).json({
            error : {
                message:error.message,
                errno: error.errno,
                code : error.code
            }
        });
    }); 
}

