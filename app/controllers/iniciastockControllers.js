const {crearIniciastock,editarIniciastock,buscarIniciastock,listarIniciastock,estadoIniciastock,eliminarIniciastock} = require('../models/iniciastockModels');

const listar=(req, res)=>{
    const id =  req.params.id;
    const sesId=req.params.sesId;
    listarIniciastock(id,'iniciastock',sesId)
    .then(valor => {
        res.json({
            valor : valor
        });
    })
    .catch(error => {
        res.status(400).json({
            error : {
                message:error.message,
                errno: error.errno,
                code : error.code
            }
        });
    }); 
}

