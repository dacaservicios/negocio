const listar=(req, res)=>{
    const id =  req.params.id;
    const sesId=req.params.sesId;
    listarParametro(id,'parametro',sesId)
    .then(valor => {
        res.json({
            valor : valor
        });
    })
    .catch(error => {
        res.status(400).json({
            error : {
                message:error.message,
                errno: error.errno,
                code : error.code
            }
        });
    }); 
}

