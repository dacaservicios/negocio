const {crearCaja,editarCaja,buscarCaja,listarCaja,estadoCaja,eliminarCaja,cuadreCaja} = require('../models/cajaModels');

const listar=(req, res)=>{
    const id =  req.params.id;
    const sesId=req.params.sesId;
    listarCaja(id,'caja',sesId)
    .then(valor => {
        res.json({
            valor : valor
        });
    })
    .catch(error => {
        res.status(400).json({
            error : {
                message:error.message,
                errno: error.errno,
                code : error.code
            }
        });
    }); 
}

