const {crearMovimiento,editarMovimiento,buscarMovimiento,listarMovimiento,estadoMovimiento,eliminarMovimiento,filtrarMovimiento,filtrarMovimientoInicio} = require('../models/movimientoModels');

const listar=(req, res)=>{
    const id =  req.params.id;
    const sesId=req.params.sesId;
    listarMovimiento(id,'movimiento',sesId)
    .then(valor => {
        res.json({
            valor : valor
        });
    })
    .catch(error => {
        res.status(400).json({
            error : {
                message:error.message,
                errno: error.errno,
                code : error.code
            }
        });
    }); 
}

